extern node_t presentTHRE;
extern node_t display1, display2, display3, display4, display5, display6;
//-------------------------------------------------------------------------------------------------------------------
//  @brief      对文件各参数进行初始化赋值
//  @param      file 要赋值的文件  类型：node_t
//  @param      intVal 数据的整型值
//  @param      floatVal 数据的浮点型值
//  @param      name    文件名字
//  @param      pos 文件出现的行数 范围2--                      7
//  @param      i 判断数据类型   none：不出现数据    dataint:数据为整型值    datafloat:数据为浮点值
//  @param      prior 指向文件的上一个文件
//  @param      next  指向文件的下一个文件
//  @param      backward  指向文件的父文件
//  @param      forward   指向文件的子文件
//  @return     node_t  赋值好的文件
//  @since      v1.0
//  Sample usage:
//-------------------------------------------------------------------------------------------------------------------
node_t MENU_fileInit(node_t file, int intVal, float floatVal, char name[10], uint8 pos,data i,struct node* prior,struct node* next,struct node* backward,struct node* forward);

//-------------------------------------------------------------------------------------------------------------------
//  @brief      实际创建文件
//  @param      无
//  @return     void
//  @since      v1.0
//  Sample usage:
//-------------------------------------------------------------------------------------------------------------------
void MENU_Init();
